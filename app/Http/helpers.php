<?php

/****************************************************************************************
PRINT VARIABLE
****************************************************************************************/

function ddAll($var) {
	echo '<pre>';
	print_r($var);
	echo '</pre>';

	exit();
}

function prf($var) {
    echo '<pre>';
    print_r($var);
    echo '</pre>';
}


/****************************************************************************************
SET ACTIVE TAB
****************************************************************************************/

function setActive($path, $active = 'active') {
	return Request::is($path) ? $active : '';
}


/****************************************************************************************
SET TODAY DATE
****************************************************************************************/

function setTodayDate() {

	date_default_timezone_set('America/Chicago'); 
	
	return date('Y-m-d');	
}
	